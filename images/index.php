function GoogleParsePics($result)
{
	preg_match_all('#\["/imgres(.*)","","(.*)","#U', $result, $result_preg);

	$images=array();
	for ($i=0; $i<count($result_preg[1]); $i++)
	{
		$tmp=str_replace(array('\x3d','\x26'),array('=','&'),$result_preg[1][$i]);

		$tmp=substr($tmp,1);
		parse_str($tmp,$tmp);


		$images[$i][url_image]=trim(str_replace(array('http://','https://'),'',$tmp[imgurl]));
		$images[$i][url_site]=trim(str_replace(array('http://','https://'),'',$tmp[imgrefurl]));


		$tmp=parse_url($tmp[imgrefurl]);

		$images[$i][site]=trim(str_replace('www.','',$tmp[host]));
		$images[$i][gstatic]=trim($result_preg[2][$i]);

	}

	return $images;
}


function ParserGooglePics($result)
{
	preg_match_all('#\["/imgres(.*)","","(.*)","(.*)","(.*)","(.*)","(.*)","","","(.*)","(.*)"#U', $result, $result_preg);
	//print_r($result_preg);

	$images=array();
	for ($i=0; $i<count($result_preg[1]); $i++)
	{
		$tmp=str_replace(array('\x3d','\x26'),array('=','&'),$result_preg[1][$i]);
		$tmp=substr($tmp,1);
		parse_str($tmp,$tmp);


		$images[$i][url]=trim($tmp[imgurl]);
		$images[$i][page]=trim($tmp[imgrefurl]);


		$tmp=parse_url($tmp[imgrefurl]);
		$images[$i][site]=trim(str_replace('www.','',$tmp[host]));


		$images[$i][thumb_code]=trim($result_preg[2][$i]);
		$images[$i][thumb_width]=trim($result_preg[4][$i]);
        $images[$i][thumb_height]=trim($result_preg[5][$i]);


		$tmp=$result_preg[6][$i];
        $tmp=str_replace('\x3c',"<",$tmp);
        $tmp=str_replace('\x3e',">",$tmp);
        $tmp=str_replace('\x26',"&",$tmp);
        $tmp=strip_tags($tmp);
        $tmp=str_replace('&#39;',"'",$tmp);
        $images[$i][text]=trim($tmp);


        $tmp=$result_preg[7][$i];
        $tmp=explode(' - ',$tmp);
        $images[$i][size]=trim(str_replace('k','',$tmp[1]));


		$tmp=explode(' &times; ',$tmp[0]);
		$images[$i][width]=trim($tmp[0]);
        $images[$i][height]=trim($tmp[1]);


        $images[$i][format]=trim($result_preg[8][$i]);
	}

	return $images;
}


